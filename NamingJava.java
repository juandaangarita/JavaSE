// Upper Camel Case se utilizan para las clases
public class NamingJava {
    public static void main(String[] args) {
        int cellphone = 333333777;
        int cellPhone = 1342345;
        System.out.println(cellphone);
        System.out.println(cellPhone);

        String $countryName = "Spain";
        String _backgroundColor = "Green";

        String currency$ = "COP";
        String background_color = "Blue";

        int POSITION = -5;
        int MAX_WIDTH = 9999;
        int MIN_WIDTH = 1;

        //Lower Camel Case
        String fullName = "Juan David Angarita";
        int sizeInCentimeters = 26;

        int i=1, j=2, k=3, m=2;
        System.out.println ((j >= i) || (k == m));
    }
}
